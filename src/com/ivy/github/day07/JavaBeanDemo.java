//	    分类:
//	       1):复杂:UI,比如Button,Panel,Window类.
//	       2):简单:domain,dao,service组件,封装数据,操作数据库,逻辑运算等.
//	           (封装有字段,并提供getter/setter)
//	    成员:
//	       1):方法:Method
//	       2):事件:event
//	       3):属性:property

